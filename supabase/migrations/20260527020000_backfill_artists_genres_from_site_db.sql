-- Reuse the existing UUID-based public.artists table.
-- Retain the legacy normalized_name so distinct artists such as "&ME" and
-- "me" are not collapsed through punctuation removal.
insert into public.artists (name, normalized_name, aliases, source)
select
  name,
  normalized_name,
  '{}'::text[],
  '1001tracklists'
from tmp_site_artists
on conflict (normalized_name, source) do update
set name       = excluded.name,
    updated_at = now();

-- Store every genre from the legacy genre catalog.
insert into public.genres (name, normalized_name)
select
  min(name) as name,
  public.dropdex_normalize_genre(name) as normalized_name
from tmp_site_genres
where public.dropdex_normalize_genre(name) <> ''
group by public.dropdex_normalize_genre(name)
on conflict (normalized_name) do nothing;

-- Also store every genre tag actually assigned to a legacy artist.
with artist_tags as (
  select
    trim(tag) as name,
    public.dropdex_normalize_genre(trim(tag)) as normalized_name
  from tmp_site_artists a
  cross join lateral unnest(string_to_array(coalesce(a.raw_genres, ''), '|')) as tag
  where nullif(trim(tag), '') is not null
)
insert into public.genres (name, normalized_name)
select min(name), normalized_name
from artist_tags
where normalized_name <> ''
group by normalized_name
on conflict (normalized_name) do nothing;

-- Build the many-to-many relationships using existing public.artists UUIDs.
with artist_tags as (
  select distinct
    a.normalized_name as artist_normalized_name,
    public.dropdex_normalize_genre(trim(tag)) as genre_normalized_name
  from tmp_site_artists a
  cross join lateral unnest(string_to_array(coalesce(a.raw_genres, ''), '|')) as tag
  where nullif(trim(tag), '') is not null
)
insert into public.artist_genres (artist_id, genre_id)
select
  a.id,
  g.id
from artist_tags t
join public.artists a
  on a.normalized_name = t.artist_normalized_name
 and a.source = '1001tracklists'
join public.genres g
  on g.normalized_name = t.genre_normalized_name
where t.genre_normalized_name <> ''
on conflict (artist_id, genre_id) do nothing;

-- Refuse partial migrations: validate that every embedded artist and every
-- recoverable site.db relationship is present before committing.
do $$
declare
  v_matched_artists integer;
  v_expected_links   integer;
  v_stored_links     integer;
begin
  select count(*)
    into v_matched_artists
  from tmp_site_artists t
  join public.artists a
    on a.normalized_name = t.normalized_name
   and a.source = '1001tracklists';

  if v_matched_artists <> 1420 then
    raise exception 'site.db artist backfill incomplete: expected 1420 matched artists, found %', v_matched_artists;
  end if;

  with expected_links as (
    select distinct
      t.normalized_name as artist_normalized_name,
      public.dropdex_normalize_genre(trim(tag)) as genre_normalized_name
    from tmp_site_artists t
    cross join lateral unnest(string_to_array(coalesce(t.raw_genres, ''), '|')) as tag
    where nullif(trim(tag), '') is not null
      and public.dropdex_normalize_genre(trim(tag)) <> ''
  )
  select count(*) into v_expected_links from expected_links;

  with expected_links as (
    select distinct
      t.normalized_name as artist_normalized_name,
      public.dropdex_normalize_genre(trim(tag)) as genre_normalized_name
    from tmp_site_artists t
    cross join lateral unnest(string_to_array(coalesce(t.raw_genres, ''), '|')) as tag
    where nullif(trim(tag), '') is not null
      and public.dropdex_normalize_genre(trim(tag)) <> ''
  )
  select count(*)
    into v_stored_links
  from expected_links e
  join public.artists a
    on a.normalized_name = e.artist_normalized_name
   and a.source = '1001tracklists'
  join public.genres g
    on g.normalized_name = e.genre_normalized_name
  join public.artist_genres ag
    on ag.artist_id = a.id
   and ag.genre_id = g.id;

  if v_expected_links <> 7880 or v_stored_links <> v_expected_links then
    raise exception 'site.db relationship backfill incomplete: expected % links, stored %', v_expected_links, v_stored_links;
  end if;

  raise notice 'site.db backfill verified: % artists and % artist/genre links attached.', v_matched_artists, v_stored_links;
end $$;

commit;

